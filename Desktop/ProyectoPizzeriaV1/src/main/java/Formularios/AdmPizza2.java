    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        String nombre = txtnombre.getText();
        String description = txtdescription.getText();
        double price = Double.parseDouble(txtprice.getText());
        boolean vegetarian = chekvegetarian.isSelected();
        boolean vegan = checkvegan.isSelected();
        boolean available = checkavailable.isSelected();

        Object[] newRow = {null, nombre, description, price, vegetarian, vegan, available};
        tableModel.addRow(newRow);
        txtnombre.setText("");
        txtdescription.setText("");
        txtprice.setText("");
        chekvegetarian.setSelected(false);
        checkvegan.setSelected(false);
        checkavailable.setSelected(false);

        try (Connection conn = ConexionGeneral.obtenerConexion()) {
            String sql = "INSERT INTO pizza (name, description, price, vegetarian, vegan, available) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, nombre);
            pstmt.setString(2, description);
            pstmt.setDouble(3, price);
            pstmt.setBoolean(4, vegetarian);
            pstmt.setBoolean(5, vegan);
            pstmt.setBoolean(6, available);

            pstmt.executeUpdate();

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnagregarActionPerformed

